import { Link, useLocation } from 'react-router-dom'
import { motion } from 'framer-motion'
import { cn } from '../../lib/utils'

interface SidebarLink {
  label: string
  href: string
  icon: React.ReactNode
}

interface SidebarProps {
  links: SidebarLink[]
  footer?: React.ReactNode
}

export function Sidebar({ links, footer }: SidebarProps) {
  const location = useLocation()

  return (
    <aside className="fixed left-0 top-0 z-30 h-screen w-64 border-r border-border bg-background-deep flex flex-col">
      {/* Logo */}
      <div className="h-16 flex items-center px-6 border-b border-border">
        <Link to="/" className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-lg bg-gradient-to-br from-accent-primary to-accent-secondary flex items-center justify-center">
            <span className="text-background-deep font-bold text-sm">NX</span>
          </div>
          <span className="font-heading font-bold text-lg gradient-text">NEMX</span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-6 px-3 overflow-y-auto">
        <ul className="space-y-1">
          {links.map((link) => {
            const isActive = location.pathname === link.href
            return (
              <li key={link.href}>
                <Link
                  to={link.href}
                  className={cn(
                    'relative flex items-center gap-3 px-3 py-2.5 rounded-lg',
                    'text-sm font-medium transition-colors duration-150',
                    isActive
                      ? 'text-accent-primary bg-accent-primary/10'
                      : 'text-text-muted hover:text-text-primary hover:bg-background-surface'
                  )}
                >
                  {isActive && (
                    <motion.div
                      layoutId="sidebar-active"
                      className="absolute left-0 top-1/2 -translate-y-1/2 w-1 h-6 rounded-full bg-accent-primary"
                      transition={{ duration: 0.2 }}
                    />
                  )}
                  <span className="shrink-0">{link.icon}</span>
                  <span>{link.label}</span>
                </Link>
              </li>
            )
          })}
        </ul>
      </nav>

      {/* Footer */}
      {footer && (
        <div className="p-4 border-t border-border">
          {footer}
        </div>
      )}
    </aside>
  )
}

